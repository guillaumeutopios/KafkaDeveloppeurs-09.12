    @Bean
    public KafkaTemplate<String, GenericRecord> avroKafkaTemplate() {
        Map<String, Object> config = new HashMap<>();
        config.put(ProducerConfig.BOOTSTRAP_SERVERS_CONFIG, bootstrapServers); // Changez l'adresse selon votre environnement
        config.put(ProducerConfig.KEY_SERIALIZER_CLASS_CONFIG, StringSerializer.class);
        config.put(ProducerConfig.VALUE_SERIALIZER_CLASS_CONFIG, KafkaAvroSerializer.class);
        config.put("schema.registry.url", "http://localhost:8081"); // URL du Schema Registry

        return new KafkaTemplate<>(new DefaultKafkaProducerFactory<>(config));
    }
